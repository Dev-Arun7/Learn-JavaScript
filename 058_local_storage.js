// Run this code on a browser console to work

// Storing a value in local storage
localStorage.setItem("name", "Arun"); // Sets the key "name" with the value "Arun"

// Retrieving the value from local storage
var result = localStorage.getItem("name"); // Gets the value associated with the key "name"

// Logging the result to the console
console.log(result); // Should output "Arun"
