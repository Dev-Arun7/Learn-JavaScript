
let fruit = "apple";

switch (fruit) {
    case "apple":
        console.log("Apples are red.");
        break;
    case "banana":
        console.log("Bananas are yellow.");
        break;
    case "orange":
        console.log("Oranges are orange.");
        break;
    case "grape":
        console.log("Grapes is purple.");
        break;
    default:
        console.log("Unknown fruit.");
        break;
}
